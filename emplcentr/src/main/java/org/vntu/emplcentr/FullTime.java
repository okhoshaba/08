package org.vntu.emplcentr;

public class FullTime extends GenInform {

  private String mainWork;
  private String expirience;
  private String speciality;

  public String getMainWork() {
    return mainWork;
  }

  public void setMainWork(String mainWork) {
    this.mainWork = mainWork;
  }

  public String getExpirience() {
    return expirience;
  }

  public void setExpirience(String expirience) {
    this.expirience = expirience;
  }

  public String getSpeciality() {
    return speciality;
  }

  public void setSpeciality(String speciality) {
    this.speciality = speciality;
  }

}

